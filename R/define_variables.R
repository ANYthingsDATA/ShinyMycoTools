# Internal helper: parse numeric, handling both "." and "," as decimal separator.
# Works on character, factor, or already-numeric vectors.
.parse_numeric <- function(x) {
  if (is.numeric(x)) return(x)
  as.double(gsub(",", ".", as.character(x)))
}

