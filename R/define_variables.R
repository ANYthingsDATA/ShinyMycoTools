#' Define Date Variable (wrapper)
#'
#' @description
#' Backward-compatible convenience wrapper that creates a normalized date
#' variable from a single input date column. Internally calls
#' `define_variables_datetime()` and returns the same three standardized outputs
#' (`gen_datetime`, `gen_date`, `gen_time`) unless you change the output names.
#'
#' @param data A data frame.
#' @param input_date Bare name or string. Date column to parse.
#' @param output_date Name of the new date variable to be created (default "gen_date").
#' @param tz Character. Target time zone for the parsed datetime (default "UTC").
#'
#' @return A modified data frame with `gen_date` (and also `gen_datetime`, `gen_time`).
#'
#' @examples
#' # data %>% define_variables_date(input_date = DateColumn)
#'
#' @import dplyr
#' @importFrom rlang ensym as_string
#' @export
define_variables_date <- function(data,
                                  input_date,
                                  output_date = "gen_date",
                                  tz = "UTC") {
  date_col <- rlang::as_string(rlang::ensym(input_date))

  define_variables_datetime(
    data = data,
    input_date = date_col,
    tz = tz,
    output_date = output_date
  )
}

