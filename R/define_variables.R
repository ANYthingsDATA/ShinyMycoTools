#' Normalize date/time from unified or split columns
#'
#' @description
#' `define_variables_datetime()` creates standardized datetime, date, and time variables
#' from either a single unified datetime column (`input_datetime`) or a pair of
#' columns (`input_date` + `input_time`). It accepts bare names or strings for
#' the input columns. When only a date is provided, time defaults to `00:00:00`.
#'
#' @param data A data frame.
#' @param input_datetime Bare name or string. Unified datetime column. Optional if `input_date` and `input_time` are provided.
#' @param input_date Bare name or string. Date column. Optional if `input_datetime` is provided.
#' @param input_time Bare name or string. Time column (e.g., "14:30", "14:30:05"). Optional; if missing, defaults to "00:00:00".
#' @param tz Character. Target time zone for the parsed datetime (default "UTC").
#' @param output_datetime Name of the output datetime column (default "gen_datetime").
#' @param output_date Name of the output date column (default "gen_date").
#' @param output_time Name of the output time column (default "gen_time").
#' @param quiet Logical. If `TRUE`, suppresses informative messages.
#'
#' @return The input data frame with added/overwritten `output_datetime`, `output_date`, and `output_time`.
#'
#' @examples
#' # 1) Unified datetime:
#' # data %>% define_variables_datetime(input_datetime = dt, tz = "Europe/Oslo")
#'
#' # 2) Split date + time:
#' # data %>% define_variables_datetime(input_date = dato, input_time = klokke)
#'
#' # 3) Only date (time defaults to midnight):
#' # data %>% define_variables_datetime(input_date = sample_date)
#'
#' @import dplyr
#' @importFrom rlang ensym as_string :=
#' @importFrom parsedate parse_date
#' @importFrom lubridate as_date hms is.POSIXt force_tz
#' @export
define_variables_datetime <- function(
    data,
    input_datetime = NULL,
    input_date = NULL,
    input_time = NULL,
    tz = "UTC",
    output_datetime = "gen_datetime",
    output_date = "gen_date",
    output_time = "gen_time",
    quiet = FALSE
) {
  # helpers to get column names (or NULL)
  get_col <- function(x) {
    if (is.null(x)) return(NULL)
    rlang::as_string(rlang::ensym(x))
  }

  dt_col   <- get_col(input_datetime)
  date_col <- get_col(input_date)
  time_col <- get_col(input_time)

  if (is.null(dt_col) && is.null(date_col)) {
    stop("Provide either `input_datetime` or `input_date` (with optional `input_time`).", call. = FALSE)
  }
  if (!is.null(dt_col) && (!is.null(date_col) || !is.null(time_col)) && !quiet) {
    message("Both unified datetime and date/time provided. Using `input_datetime` and ignoring split inputs.")
  }

  out_dt   <- output_datetime
  out_date <- output_date
  out_time <- output_time

  out <- dplyr::mutate(data,
                       # 1) Build a normalized POSIXct (out_dt)
                       !!out_dt := {
                         if (!is.null(dt_col)) {
                           # Unified datetime path
                           x <- .data[[dt_col]]
                           # If character/factor -> parse; if already POSIX -> keep; if Date -> make midnight
                           if (inherits(x, "POSIXt")) {
                             lubridate::force_tz(x, tzone = tz)
                           } else if (inherits(x, "Date")) {
                             lubridate::force_tz(as.POSIXct(x), tzone = tz)
                           } else {
                             lubridate::parse_date_time(as.character(x), orders = c("YmdHMS", "Ymd HMS", "dmYHMS", "mdYHMS", "Ymd"), tz = tz, quiet = TRUE)
                           }
                         } else {
                           # Split date + time path
                           d <- .data[[date_col]]
                           # Parse date robustly
                           d_parsed <- if (inherits(d, "Date")) {
                             d
                           } else {
                             parsedate::parse_date(as.character(d))
                           }

                           # Time handling
                           if (is.null(time_col)) {
                             t_str <- rep("00:00:00", length(d_parsed))
                           } else {
                             t_raw <- .data[[time_col]]
                             # If it's hms, format; else assume character-ish and keep
                             if (inherits(t_raw, "hms")) {
                               t_str <- format(t_raw)
                             } else {
                               t_str <- ifelse(is.na(t_raw) | trimws(as.character(t_raw)) == "",
                                               "00:00:00",
                                               as.character(t_raw))
                             }
                           }

                           combo <- ifelse(is.na(d_parsed), NA_character_,
                                           paste0(as.character(d_parsed), " ", t_str))
                           lubridate::parse_date_time(combo, orders = c("YmdHMS", "Ymd HMS", "dmYHMS", "mdYHMS", "Ymd"), tz = tz, quiet = TRUE)
                         }
                       },
                       # 2) Derive date and time from the normalized POSIXct
                       !!out_date := lubridate::as_date(.data[[out_dt]]),
                       !!out_time := {
                         dtv <- .data[[out_dt]]
                         ifelse(is.na(dtv), NA_character_, format(dtv, "%H:%M:%S"))
                       }
  )

  out
}

