#' Define Sensor ID Variable
#'
#' This function takes a data frame and defines a sensor ID variable based on the provided input
#' sensor and port columns. It can create a sensor ID by concatenating the sensor and port values
#' with an underscore or use the sensor column directly.
#'
#' @param data The input data frame.
#' @param input_sensor The name or expression of the sensor column.
#' @param input_port The name or expression of the port column (optional).
#' @param output_sensorID The name of the new sensor ID variable to be created.
#'
#' @import dplyr
#'
#' @return A modified data frame with the new sensor ID variable added.
#'
#' @examples
#' # Example usage with sensor and port columns:
#' data_with_sensorID <- define_variables_sensorID(
#'   data = my_data, input_sensor = Sensor,
#'   input_port = Port, output_sensorID = SensorID
#' )
#'
#' # Example usage with only a sensor column:
#' data_with_sensorID <- define_variables_sensorID(
#'   data = my_data, input_sensor = Sensor,
#'   output_sensorID = gen_SensorID
#' )
#'
#' @export
define_variables_sensorID <- function(data,
                                      input_sensor,
                                      input_port,
                                      output_sensorID = gen_sensorID) {
  if (!missing(input_sensor) & missing(input_port)) {
    data %>%
      dplyr::mutate(
        {{ output_sensorID }} := as.character({{ input_sensor }})
      )
  } else if (!missing(input_sensor) & !missing(input_port)) {
    data %>%
      dplyr::mutate(
        {{ output_sensorID }} := paste0({{ input_sensor }}, "_", {{ input_port }})
      )
  } else {
    stop("No sensor ID column to define is specified")
  }
}

